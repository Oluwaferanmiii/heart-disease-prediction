import tkinter as tk

def check_tkinter():
  root = tk.Tk()
  root.title("Tkinter Check")
  label = tk.Label(root, text="Tkinter is installed and working!")
  label.pack(pady=20, padx=20)
  root.mainloop()

check_tkinter()